/**
 * Electron 主进程入口
 *
 * 负责：
 * - 单实例锁定
 * - 应用生命周期管理
 * - 窗口创建
 * - IPC 处理器注册
 */

import { app, BrowserWindow } from 'electron';
import { logger } from '@wentong/utils';
import { createMainWindow, getMainWindow } from './window';
import { setupIpcHandlers } from './ipc/index';

// ---------------------------------------------------------------------------
// 单实例锁定
// ---------------------------------------------------------------------------

const gotTheLock = app.requestSingleInstanceLock();

if (!gotTheLock) {
  // 已有实例运行，退出当前实例
  logger.info('应用已有实例在运行，退出当前实例');
  app.quit();
} else {
  // ---------------------------------------------------------------------------
  // 第二实例启动时聚焦已有窗口
  // ---------------------------------------------------------------------------

  app.on('second-instance', () => {
    const win = getMainWindow();
    if (win) {
      if (win.isMinimized()) {
        win.restore();
      }
      win.focus();
    }
  });

  // ---------------------------------------------------------------------------
  // 应用就绪
  // ---------------------------------------------------------------------------

  app.whenReady().then(() => {
    logger.info('应用启动中...');

    // 注册 IPC 处理器
    setupIpcHandlers();
    logger.info('IPC 处理器注册完成');

    // 创建主窗口
    createMainWindow();
    logger.info('主窗口创建完成');

    logger.info('应用启动完成');
  });

  // ---------------------------------------------------------------------------
  // macOS activate 事件处理
  // 点击 Dock 图标时如果没有窗口则创建一个
  // ---------------------------------------------------------------------------

  app.on('activate', () => {
    if (BrowserWindow.getAllWindows().length === 0) {
      createMainWindow();
    }
  });

  // ---------------------------------------------------------------------------
  // 所有窗口关闭时退出应用（非 macOS）
  // ---------------------------------------------------------------------------

  app.on('window-all-closed', () => {
    if (process.platform !== 'darwin') {
      logger.info('所有窗口已关闭，应用退出');
      app.quit();
    }
  });

  // ---------------------------------------------------------------------------
  // 应用退出前清理
  // ---------------------------------------------------------------------------

  app.on('before-quit', () => {
    logger.info('应用即将退出');
  });
}
